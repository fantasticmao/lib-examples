package cn.fantasticmao.demo.java.algorithm.leetcode;

/**
 * ListNode
 *
 * @author fantasticmao
 * @since 2022-07-05
 */
public class ListNode {
    public int val;
    public ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
