package cn.fantasticmao.demo.java.others.freemarker;

/**
 * Product
 *
 * @author fantasticmao
 * @since 2021-12-19
 */
public class Product {
    private String url;
    private String name;

    public Product() {
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
